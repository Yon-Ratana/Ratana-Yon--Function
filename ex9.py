
# EX9.Create function to find average of price
def sum(products):
    num1=0
    num2=0
    numN=0   
    for i in range(len(products)):
        average = int(num1 + num2+ ... + numN)
    return average
products = [
    {"name": "Apple", "price": 20},
    {"name": "Orange", "price": 24},
]
print(sum(products))
